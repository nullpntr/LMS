    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if((title.getText().isEmpty() || price.getText().isEmpty() || (author1.getSelectedIndex()==0) && (author2.getSelectedIndex()==0) && (author3.getSelectedIndex()==0))){
        JOptionPane.showMessageDialog(rootPane, "Fill all essential feilds.");
        }else{
            String ttl = (String)title.getText();
            String pb = (String)pub.getSelectedItem();
            String at1 = (String)author1.getSelectedItem();
            String at2 = (String)author2.getSelectedItem();
            String at3 = (String)author3.getSelectedItem();
            String brn = (String)branch.getSelectedItem();
            int pr = (int)(Double.parseDouble(price.getText()));
            int edn = (int)(Double.parseDouble(ed.getText()));
            boolean b = new BookDB().save(ttl, pb, pr, edn, at1, at2, at3, brn);
            if(b){
                JOptionPane.showMessageDialog(this, "Book added in Inventory.");
                title.setText("");
                price.setText("");
                ed.setText("");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed


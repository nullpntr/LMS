     private void loadAuthors(){
         author1.removeAllItems();
             author2.removeAllItems();
             author3.removeAllItems();
         for(String a : new BookDB().getAuthors()){
             author1.addItem(a);
             author2.addItem(a);
             author3.addItem(a);
         }
     }

    private void loadBranches(){
        branch.removeAllItems();
         for(String b : new BookDB().getBranchs()){

             branch.addItem(b);

         }
     }

    private void loadPublication(){
        pub.removeAllItems();
        for(String b : new BookDB().getPublications()){
            pub.addItem(b);
        }
    }
